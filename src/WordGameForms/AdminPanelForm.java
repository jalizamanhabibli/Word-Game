    private void refreshTable(List list) {
        List<Competitor.CompetitorInfo> competitorInfos = list;

        Object[] columnName = {"Id", "Name", "Surname", "Point", "Position"};

        Object[][] data = new Object[competitorInfos.size()][columnName.length];
        for (int i = 0; i < competitorInfos.size(); i++) {
            data[i][0] = competitorInfos.get(i).getId();
            data[i][1] = competitorInfos.get(i).getName();
            data[i][2] = competitorInfos.get(i).getSurname();
            data[i][3] = competitorInfos.get(i).getPoint();
            data[i][4] = competitorInfos.get(i).isPosition();
        }

        tblCompetitor.setModel(new DefaultTableModel(data, columnName) {

            @Override
            public boolean isCellEditable(int row, int column) {
                if (column == 4) {
                    return true;
                }
                return false;
            }

            @Override
            public Class getColumnClass(int column) {
                switch (column) {
                    case 0:
                        return Integer.class;
                    case 3:
                        return Integer.class;
                    case 4:
                        return Boolean.class;
                    default:
                        return String.class;
                }
            }
        });
    }

