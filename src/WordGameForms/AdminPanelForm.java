    private void btnEdirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdirActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Əminmiziniz?", "", JOptionPane.YES_NO_OPTION) == 0) {
            List<Competitor.CompetitorInfo> competitorInfos = competitorDatabase.competitorInfo(conn);
            for (int i = 0; i < tblCompetitor.getRowCount(); i++) {
                if ((Boolean) tblCompetitor.getValueAt(i, 4) != competitorInfos.get(i).isPosition()) {
                    competitorInfos.get(i).setPosition(!competitorInfos.get(i).isPosition());
                    competitorDatabase.updateCompetitorInfo(competitorInfos.get(i).getId(), competitorInfos.get(i).isPosition(), conn);
                }
            }
            refreshTable(competitorDatabase.competitorInfo(conn));
        }
    }//GEN-LAST:event_btnEdirActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Silməy istədiyinizdən əminmisiniz?", "", JOptionPane.YES_NO_OPTION) == 0) {
            try {
                int id = (int) tblCompetitor.getValueAt(tblCompetitor.getSelectedRow(), 0);
//                System.out.println(id);
            competitorDatabase.delete(id, conn);
            refreshTable(competitorDatabase.competitorInfo(conn));
            } catch (Exception ex) {

            }

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

